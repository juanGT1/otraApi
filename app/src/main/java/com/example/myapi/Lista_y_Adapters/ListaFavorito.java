package com.example.myapi.Lista_y_Adapters;

import java.io.Serializable;

public class ListaFavorito implements Serializable {


    private String title ;
    private String poster_path; //imagen
    private String popularity; //cantidad de vists
    private String vote_average; // calificacion
    private String overview; // Descripcion


    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getPoster_path() {
        return poster_path;
    }

    public void setPoster_path(String poster_path) {
        this.poster_path = poster_path;
    }

    public String getPopularity() {
        return popularity;
    }

    public void setPopularity(String popularity) {
        this.popularity = popularity;
    }

    public String getVote_average() {
        return vote_average;
    }

    public void setVote_average(String vote_average) {
        this.vote_average = vote_average;
    }

    public String getOverview() {
        return overview;
    }

    public void setOverview(String overview) {
        this.overview = overview;
    }
}
